def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Register a demo candidate and apply to seeded jobs.")
    parser.add_argument("--base-url", default=DEFAULT_BASE_URL, help="Web API base URL.")
    parser.add_argument("--name", default=DEFAULT_NAME, help="Candidate name.")
    parser.add_argument("--email", default=DEFAULT_EMAIL, help="Candidate email.")
    parser.add_argument("--password", default=DEFAULT_PASSWORD, help="Candidate password.")
    parser.add_argument(
        "--job-id",
        action="append",
        type=int,
        dest="job_ids",
        help="Apply to a specific job id. Can be repeated.",
    )
    return parser.parse_args()


async def register_or_reuse_candidate(
    client: httpx.AsyncClient,
    *,
    name: str,
    email: str,
    password: str,
) -> dict[str, Any]:
    response = await client.post(
        "/user/register",
        json={
            "name": name,
            "email": email,
            "password": password,
        },
    )
    if response.status_code == 201:
        payload = response.json()
        print(f"[register] created candidate user id={payload['id']} username={payload['username']}")
        return payload

    if response.status_code == 422:
        detail = response.json().get("detail")
        if detail == "Email is already registered":
            print(f"[register] candidate {email} already exists, continue with login")
            return {}

    raise RuntimeError(f"Failed to register candidate: {response.status_code} {response.text}")


async def login_candidate(
    client: httpx.AsyncClient,
    *,
    email: str,
    password: str,
) -> str:
    response = await client.post(
        "/login",
        data={"username": email, "password": password},
        headers={"Content-Type": "application/x-www-form-urlencoded"},
    )
    if response.status_code != 200:
        raise RuntimeError(f"Failed to login candidate: {response.status_code} {response.text}")
    access_token = response.json()["access_token"]
    print("[login] candidate login succeeded")
    return access_token


async def fetch_current_user(
    client: httpx.AsyncClient,
    *,
    access_token: str,
) -> dict[str, Any]:
    response = await client.get(
        "/user/me",
        headers={"Authorization": f"Bearer {access_token}"},
    )
    if response.status_code != 200:
        raise RuntimeError(f"Failed to fetch current user: {response.status_code} {response.text}")
    payload = response.json()
    print(f"[me] user id={payload['id']} username={payload['username']} email={payload['email']}")
    return payload


async def ensure_resume_asset(*, user_id: int, email: str) -> Asset:
    async with local_session() as session:
        result = await session.execute(
            select(Asset).where(
                Asset.owner_type == "user",
                Asset.owner_id == user_id,
                Asset.module == "candidate",
                Asset.type == "file",
                Asset.original_name == "demo-resume.pdf",
                Asset.is_deleted.is_(False),
            )
        )
        existing = result.scalar_one_or_none()
        if existing is not None:
            print(f"[resume] reuse asset id={existing.id}")
            return existing

        storage_key = f"candidate/file/{uuid4().hex}-demo-resume.pdf"
        root = get_asset_storage_root()
        path = root / storage_key
        path.parent.mkdir(parents=True, exist_ok=True)
        content = (
            f"Demo resume for {email}\n"
            "This file is generated by run_client_apply_demo.py for apply flow verification.\n"
        ).encode("utf-8")
        path.write_bytes(content)

        asset = Asset(
            type="file",
            module="candidate",
            owner_type="user",
            owner_id=user_id,
            original_name="demo-resume.pdf",
            storage_key=storage_key,
            mime_type="application/pdf",
            file_size=len(content),
            data={"generated_by": "run_client_apply_demo"},
        )
        session.add(asset)
        await session.commit()
        await session.refresh(asset)
        print(f"[resume] created asset id={asset.id}")
        return asset


async def resolve_job_ids(client: httpx.AsyncClient, *, requested_ids: list[int] | None) -> list[int]:
    if requested_ids:
        return requested_ids

    response = await client.get("/jobs")
    if response.status_code != 200:
        raise RuntimeError(f"Failed to list jobs: {response.status_code} {response.text}")
    items = response.json().get("items", [])
    matched_ids: list[int] = []
    for title in DEFAULT_JOB_TITLES:
        matched = next((item for item in items if item.get("title") == title), None)
        if matched is None:
            raise RuntimeError(f"Could not find seeded job titled: {title}")
        matched_ids.append(int(matched["id"]))
    print(f"[jobs] resolved demo jobs {matched_ids}")
    return matched_ids


