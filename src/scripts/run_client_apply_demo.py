async def submit_application(
    client: httpx.AsyncClient,
    *,
    access_token: str,
    job_id: int,
    items: list[dict[str, Any]],
) -> dict[str, Any]:
    response = await client.post(
        f"/jobs/{job_id}/apply",
        headers={"Authorization": f"Bearer {access_token}"},
        json={"items": items},
    )
    if response.status_code != 200:
        raise RuntimeError(f"Failed to apply job {job_id}: {response.status_code} {response.text}")
    payload = response.json()
    print(
        "[apply] job_id={job_id} application_id={application_id} talent_profile_id={talent_profile_id} "
        "talent_profile_created={talent_profile_created} auto_merged={auto_merged}".format(**payload, job_id=job_id)
    )
    return payload


async def fetch_job_detail(client: httpx.AsyncClient, *, job_id: int) -> dict[str, Any]:
    response = await client.get(f"/jobs/{job_id}")
    if response.status_code != 200:
        raise RuntimeError(f"Failed to fetch job detail {job_id}: {response.status_code} {response.text}")
    payload = response.json()
    print(f"[job] {job_id} title={payload['title']} form_template_id={payload['form_template_id']} fields={len(payload['form_fields'])}")
    return payload


async def main() -> None:
    args = parse_args()
    try:
        async with httpx.AsyncClient(base_url=args.base_url.rstrip("/"), timeout=30.0) as client:
            await register_or_reuse_candidate(
                client,
                name=args.name,
                email=args.email,
                password=args.password,
            )
            access_token = await login_candidate(client, email=args.email, password=args.password)
            current_user = await fetch_current_user(client, access_token=access_token)
            resume_asset = await ensure_resume_asset(user_id=int(current_user["id"]), email=args.email)

            job_ids = await resolve_job_ids(client, requested_ids=args.job_ids)
            applications: list[dict[str, Any]] = []
            for index, job_id in enumerate(job_ids):
                await fetch_job_detail(client, job_id=job_id)
                items = build_application_items(
                    job_index=index,
                    candidate_name=args.name,
                    email=args.email,
                    resume_asset_id=resume_asset.id,
                )
                applications.append(
                    await submit_application(
                        client,
                        access_token=access_token,
                        job_id=job_id,
                        items=items,
                    )
                )

            summary = {
                "candidate": {
                    "id": current_user["id"],
                    "name": current_user["name"],
                    "username": current_user["username"],
                    "email": current_user["email"],
                },
                "resume_asset_id": resume_asset.id,
                "job_ids": job_ids,
                "applications": applications,
            }
            print(json.dumps(summary, ensure_ascii=False, indent=2))
    finally:
        await async_engine.dispose()


if __name__ == "__main__":
    asyncio.run(main())
